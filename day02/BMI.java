package day02;

class BMI {
    double weight;
    double height;
    
    void setWeight(double weight) {
        this.weight=weight;
    }
    
    double getWeight() {
        return weight;
    }
    
    void setHeight(double height) {
        this.height=height;
    }
    
    double getHeight() {
        return height;
    }
}
